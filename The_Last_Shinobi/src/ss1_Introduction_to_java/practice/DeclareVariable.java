package ss1_Introduction_to_java.practice;

public class DeclareVariable {
    public static void main(String[] args) {
        int numberI = 10;
        float numberF = 20.5f;
        double numberD = 20.5;
        boolean numberB = true;
        char numberC = 'a';
        String string = "Hà Nội";
        System.out.println("i = " + numberI);
        System.out.println("f = " + numberF);
        System.out.println("d = " + numberD);
        System.out.println("b = " + numberB);
        System.out.println("c = " + numberC);
        System.out.println("s = " + string);
    }
}
